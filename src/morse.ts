export function decodeMorse(text) {
    let regExp = /[a-zA-Z]/g;

    if (regExp.test(text)) {
        let ref = {
            a: '.-',
            b: '-...',
            c: '-.-.',
            d: '-..',
            e: '.',
            f: '..-.',
            g: '--.',
            h: '....',
            i: '..',
            j: '.---',
            k: '-.-',
            l: '.-..',
            m: '--',
            n: '-.',
            o: '---',
            p: '.--.',
            q: '--.-',
            r: '.-.',
            s: '...',
            t: '-',
            u: '..-',
            w: '.--',
            x: '-..-',
            y: '-.--',
            z: '--..',
            '1': '.----',
            '2': '..---',
            '3': '...--',
            '4': '....-',
            '5': '.....',
            '6': '-....',
            '7': '--...',
            '8': '---..',
            '9': '----.',
            '0': '-----',
        };

        return text
            .toLowerCase()
            .split(' ')
            .map(el => {
                return ref[el] ? ref[el] : el;
            })
            .join(' ');
    } else {
        let ref = {
            '.-': 'a',
            '-...': 'b',
            '-.-.': 'c',
            '-..': 'd',
            '.': 'e',
            '..-.': 'f',
            '--.': 'g',
            '....': 'h',
            '..': 'i',
            '.---': 'j',
            '-.-': 'k',
            '.-..': 'l',
            '--': 'm',
            '-.': 'n',
            '---': 'o',
            '.--.': 'p',
            '--.-': 'q',
            '.-.': 'r',
            '...': 's',
            '-': 't',
            '..-': 'u',
            '...-': 'v',
            '.--': 'w',
            '-..-': 'x',
            '-.--': 'y',
            '--..': 'z',
            '.----': '1',
            '..---': '2',
            '...--': '3',
            '....-': '4',
            '.....': '5',
            '-....': '6',
            '--...': '7',
            '---..': '8',
            '----.': '9',
            '-----': '0',
        };

        return text
            .split('   ')
            .map(a =>
                a
                    .split(' ')
                    .map(b => ref[b])
                    .join(''),
            )
            .join(' ');
    }
}
